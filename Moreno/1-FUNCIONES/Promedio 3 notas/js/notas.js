
function notas(pnota1, pnota2, pnota3) { // con parametros
    let nota1=parseFloat(pnota1);
    let nota2=parseFloat(pnota2);
    let nota3=parseFloat(pnota3);
    let promedio;
    promedio=(nota1+nota2+nota3)/3;
    return promedio
}
